#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		length;
	char	*dup;
	int		i;

	i = 0;
	length = ft_strlen(src) + 1;
	dup = malloc(sizeof(char) * length);
	if (!dup)
		return (NULL);
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
